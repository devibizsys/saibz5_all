    @Override
    protected boolean onFillEntityKeyValue(Registry et,boolean bTempMode) throws Exception {
        StringBuilderEx sb  = new StringBuilderEx();
        //属性 RegistryName - 系统目录
        Object objRegistryName = et.get(Registry.FIELD_REGISTRYNAME);
        if(objRegistryName==null)
            objRegistryName = "__EMTPY__";
        sb.append("%1$s", objRegistryName);
        //属性 Sector - 目录
        sb.append("||");
        Object objSector = et.get(Registry.FIELD_SECTOR);
        if(objSector==null)
            objSector = "__EMTPY__";
        sb.append("%1$s", objSector);
        String strValue = sb.toString();
        et.set(this.getRegistryDEModel().getKeyDEField().getName(),KeyValueHelper.genUniqueId(strValue));
        return true;
    }


